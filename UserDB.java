
public class UserDB {
	public String fName;
	public String lName;
	public String bDay;
	public String address;
	public int phoneNum;
	public String email;
	public String username;
	public String password;
	public String idNum;
	
	public String toString()
	{
		return fName + " " + lName + " " + bDay + " " + address + " " + phoneNum + " " + email + " " + username + " " + password + " " + idNum;
	}
}
